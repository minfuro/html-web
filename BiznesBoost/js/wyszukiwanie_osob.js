function wiecej_pokaz(){
  if (on_off === 1){
    kontener.innerHTML = domyslnyFragment + rozszerzonyFragment;
    buton.innerText = "Pokaż Mniej";
    on_off-=1;
  } else{
    kontener.innerHTML = domyslnyFragment;
    buton.innerText = "Pokaż Więcej";
    on_off+=1;
  }
}